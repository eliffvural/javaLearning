package forDongusu;

public class forDongu {
    public static void main(String[] args) {
        
        //for dongusunde dongu tekrarini saglayan bir sayac vardir.
        //bu sayac sayesinde dongudeki tekrar sayisi sabitlenmistir.
        //yani sayac sayesinde  dongunun ne kadar tekrar edilecegi en basindan bellidir.


        int i;
        for(i=1;i<=5;i++){
            System.out.println(i+". kez isletildi.");
        }

        //1 ile 5 arasindaki sayac degerlerini yazdirmak uzere for dongusu yazalim:

        int sayac;
        for(sayac=1;sayac<=5;sayac++){
            System.out.println("Sayac: "+sayac);
        }

    }
    
}
